            public void setValueFormula(Hashtable table, String[]dependence) throws boRuntimeException
            {
                if(getParent().alreadyCalculated(table, dependence)) 
                {                       
                    if(getParent().isWaiting(table, "selected_objects"))
                    {
                       getParent().setCalculated(table, "selected_objects");
                       this.setValue(boConvertUtils.convertToBigDecimal(formula(), this), false );
                    }
                    else if(!getParent().isCalculated(table, "selected_objects"))
                    {
                        getParent().setCalculated(table, "selected_objects");
                        this.setValue(boConvertUtils.convertToBigDecimal(formula(), this), table );
                    }
                }
                else if(getParent().dependsFromWaiting(table, dependence))
                {
                    if(getParent().onChangeSubmit("selected_objects"))
                    {
                        getParent().setCalculated(table, "selected_objects"); 
                    }
                    else
                    {
                        getParent().clear(table, "selected_objects"); 
                        this.setValue(boConvertUtils.convertToBigDecimal(formula(), this), false );
                    }
                }
                else
                {   
                    this.setValue(boConvertUtils.convertToBigDecimal(formula(), this), false );
                    if(getParent().onChangeSubmit("selected_objects"))
                    {                 
                        getParent().setWaiting(table, "selected_objects");
                        getParent().calculateFormula(new Hashtable(table), "selected_objects");
                        //getParent().setCalculated(table, "selected_objects");
                    }                    
                }
            }

