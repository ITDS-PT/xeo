    public bridgeHandler getBridge( String bridgeName ) 
    {
        boBridgesArray    brig = super.getBridges();        	
        bridgeHandler     ret  = brig.get(bridgeName);
        if( ret == null )
        {
        
        	if( bridgeName.equals("variables") && (ret=brig.get("variables"))==null )
        	{
            	brig.add( ret=new BridgeHandlervariables(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"variables") ) , this ) );        
        	}
        	if( bridgeName.equals("participants") && (ret=brig.get("participants"))==null )
        	{
            	brig.add( ret=new BridgeHandlerparticipants(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"participants") ) , this ) );        
        	}
        	if( bridgeName.equals("workHistory") && (ret=brig.get("workHistory"))==null )
        	{
            	brig.add( ret=new BridgeHandlerworkHistory(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"workHistory") ) , this ) );        
        	}
        	if( bridgeName.equals("access") && (ret=brig.get("access"))==null )
        	{
            	brig.add( ret=new BridgeHandleraccess(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"access") ) , this ) );        
        	}
        	if( bridgeName.equals("KEYS") && (ret=brig.get("KEYS"))==null )
        	{
            	brig.add( ret=new BridgeHandlerKEYS(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"KEYS") ) , this ) );        
        	}
        	if( bridgeName.equals("KEYS_PERMISSIONS") && (ret=brig.get("KEYS_PERMISSIONS"))==null )
        	{
            	brig.add( ret=new BridgeHandlerKEYS_PERMISSIONS(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"KEYS_PERMISSIONS") ) , this ) );        
        	}
        	if( bridgeName.equals("message") && (ret=brig.get("message"))==null )
        	{
            	brig.add( ret=new BridgeHandlermessage(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"message") ) , this ) );        
        	}
        	if( ret == null )
        	{
				ret = super.getBridge( bridgeName );
        	}
        }
        return ret;
    }
   
    
    
