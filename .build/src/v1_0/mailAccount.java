    public bridgeHandler getBridge( String bridgeName ) 
    {
        boBridgesArray    brig = super.getBridges();        	
        bridgeHandler     ret  = brig.get(bridgeName);
        if( ret == null )
        {
        
        	if( bridgeName.equals("folder") && (ret=brig.get("folder"))==null )
        	{
            	brig.add( ret=new BridgeHandlerfolder(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"folder") ) , this ) );        
        	}
        	if( bridgeName.equals("spam") && (ret=brig.get("spam"))==null )
        	{
            	brig.add( ret=new BridgeHandlerspam(new DataResultSet( getDataRow().getRecordChild( getEboContext() ,"spam") ) , this ) );        
        	}
        	if( ret == null )
        	{
				ret = super.getBridge( bridgeName );
        	}
        }
        return ret;
    }
   
    
    
